package academy.learnprogramming;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 3;
        int b = 5 * --a;
        int c = 5 * a--;

        System.out.println(b);
        System.out.println(c);

        int i = 5 /2;
        System.out.println(i);

        double j = 5/2;
        System.out.println(j);

        //6 - 10 % 1 - 2 * 12 % 5 - 2 * 3
//        Precedence order: *, / and %

    }
}
